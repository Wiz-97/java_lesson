package koreait.day01;

public class C01_Print {
	//프로그램 실행 - main 메소드 필요
	public static void main(String[] args) {
		System.out.println("Hello World");//문자열 데이터
		System.out.println(3.14);//실수 데이터
		System.out.println(55);//정수 데이터
		System.out.println(141+4869);//수식(덧셈)
		System.out.println(true);//논리(boolean) 데이터
		System.out.println("Hello"+" World");//문자열의 + 기능 : 문자열의 연결
		System.out.println("나는 " + 23 + "세 입니다.");//문자열 + 정수 ==> +에 문자열이 포함되어있으면 결과값도 문자열
		
	}
}
